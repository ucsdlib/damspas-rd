# frozen_string_literal: true
class SolrDocument
  include Blacklight::Solr::Document
  include Blacklight::Gallery::OpenseadragonSolrDocument

  # Adds Hyrax behaviors to the SolrDocument.
  include Hyrax::SolrDocumentBehavior

  # Adds custom behaviors from custom properties define in schema
  include SchemaSolrDocumentBehavior

  # self.unique_key = 'id'

  # Email uses the semantic field mappings below to generate the body of an email.
  SolrDocument.use_extension(Blacklight::Document::Email)

  # SMS uses the semantic field mappings below to generate the body of an SMS email.
  SolrDocument.use_extension(Blacklight::Document::Sms)

  # DublinCore uses the semantic field mappings below to assemble an OAI-compliant Dublin Core document
  # Semantic mappings of solr stored fields. Fields may be multi or
  # single valued. See Blacklight::Document::SemanticFields#field_semantics
  # and Blacklight::Document::SemanticFields#to_semantic_values
  # Recommendation: Use field names from Dublin Core
  use_extension(Blacklight::Document::DublinCore)

  # Do content negotiation for AF models.

  use_extension(Hydra::ContentNegotiation)

  def visibility
    if rights_override?
      @visibility ||= VisibilityService.visibility_value(fetch(Solrizer.solr_name('rights_override'), []).first)
    else
      super
    end
  end

  def rights_override?
    fetch(Solrizer.solr_name('rights_override'), []).present?
  end

  def creator
    fetch(Solrizer.solr_name('creator'), [])
  end

  # override itemtype for schema.org
  def itemtype
    Hyrax::ResourceTypesService.microdata_type(resource_type.first) if !resource_type.nil? && !resource_type.empty?
  end
end
